    private void passBookID() throws SQLException, IOException, NullPointerException {
        int row = jTable1.getSelectedRow();
        if (row != -1) {
            int bid = (int) jTable1.getModel().getValueAt(row, 0);

            DbConnector dbc = new DbConnector();
            Connection conn = dbc.Connects();
            int time_purchased = 0;
            //int row = SentMessageTable.getSelectedRow();
            //int rowNum = (int) SentMessageTable.getModel().getValueAt(row, 0);
            String sql = "SELECT POINT_BALANCE FROM UserInfo  WHERE username = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, username);
            ResultSet rs = pst.executeQuery();
            //JFrame frame = new JFrame("Purchase Time for the Book");
            //String time = JOptionPane.showInputDialog(frame, "For how long would you rent the book? (in seconds)");
            int user_points = 0;
            int book_points = 0;

            while (rs.next()) {
                user_points = rs.getInt("POINT_BALANCE");
            }
            String sql_shared = "SELECT SHARED_TIME FROM SHAREDBOOKS WHERE BOOKID = ? AND USERNAME = ?";
            PreparedStatement pst_shared = conn.prepareStatement(sql_shared);
            pst_shared.setInt(1, bid);
            pst_shared.setString(2, username);
            ResultSet rs_shared = pst_shared.executeQuery();
            while (rs_shared.next()) {
                time_purchased = rs_shared.getInt("SHARED_TIME");
            }
            if (time_purchased > 0) {

                JOptionPane.showMessageDialog(null, "It is a shared book \n You have " + time_purchased + " seconds to read the book");
                String sql_update_shared = "UPDATE SHAREDBOOKS SET SHARED_TIME = ? WHERE BOOKID = ? AND USERNAME = ?";
                PreparedStatement pst_update_shared = conn.prepareStatement(sql_update_shared);
                pst_update_shared.setInt(1, 0);
                pst_update_shared.setInt(2, bid);
                pst_update_shared.setString(3, username);
                pst_update_shared.executeUpdate();
                BookOpened bo = new BookOpened(bid, time_purchased);
                bo.setVisible(true);
                updateInfo(bid, time_purchased);
            } else {
                //JFrame frame = new JFrame("Purchase Time for the Book");
                String time = JOptionPane.showInputDialog(null, "For how long would you rent the book? (in seconds)");
                if (time != null && time.isEmpty() == false) {
                    if (time.matches("[0-9]+") && time.length() > 0) {
                        time_purchased = Integer.parseInt(time);

                        String sql2 = "SELECT READING_POINTS FROM BOOKINFO WHERE BOOKID = ?";
                        PreparedStatement pst2 = conn.prepareStatement(sql2);
                        pst2.setInt(1, bid);
                        ResultSet rs2 = pst2.executeQuery();
                        while (rs2.next()) {
                            book_points = rs2.getInt("READING_POINTS");
                        }
                        if (user_points > (time_purchased * book_points)) {
                            user_points = user_points - (time_purchased * book_points);
                            String sql3 = "UPDATE USERINFO SET POINT_BALANCE = ? WHERE USERNAME = ?";
                            PreparedStatement statement = conn.prepareStatement(sql3);
                            statement.setInt(1, user_points);

                            statement.setString(2, username);
                            statement.executeUpdate();
                            BookOpened bo = new BookOpened(bid, time_purchased);
                            bo.setVisible(true);

                            updateInfo(bid, time_purchased);

                        } else {
                            JOptionPane.showMessageDialog(null, "Not enough points");
                        }

                    } else {
                        JOptionPane.showMessageDialog(null, "The input has to be in number format");
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "No book selected");
        }

    }

    private void updateInfo(int bid, int time_purchased) throws SQLException {
        DbConnector dbc = new DbConnector();
        Connection conn = dbc.Connects();
        int reading_history = 0;
        String sql7 = "SELECT READING_COUNTS FROM BOOKINFO where BOOKID = ?";
        PreparedStatement pst7 = conn.prepareStatement(sql7);
        pst7.setInt(1, bid);
        ResultSet rs7 = pst7.executeQuery();
        int reading_count = 0;
        while (rs7.next()) {
            reading_count = rs7.getInt("READING_COUNTS");

        }
        reading_count++;

        String sql8 = "UPDATE BOOKINFO SET READING_COUNTS = ? WHERE BOOKID = ?";
        PreparedStatement pst8 = conn.prepareStatement(sql8);
        pst8.setInt(1, reading_count);
        pst8.setInt(2, bid);
        pst8.executeUpdate();

        Date date = new Date();//Calendar.getInstance().getTime();
        java.sql.Date sqlDate = new java.sql.Date(date.getTime());
        String sql0 = "UPDATE BOOKINFO SET LAST_DATE_READ = ? WHERE BOOKID = ? ";
        PreparedStatement st = conn.prepareStatement(sql0);
        st.setDate(1, sqlDate);
        st.setInt(2, bid);
        st.executeUpdate();

        String sql5 = "SELECT READING_DURATION FROM READINGHISTORY WHERE USERNAME = ? AND BOOKID = ?";
        PreparedStatement statement1 = conn.prepareStatement(sql5);
        statement1.setString(1, username);
        statement1.setInt(2, bid);

        ResultSet rs1 = statement1.executeQuery();
        if (rs1.next() == false) {
            String sql6 = "INSERT INTO READINGHISTORY (USERNAME, BOOKID) VALUES (?, ?)";
            PreparedStatement pst6 = conn.prepareStatement(sql6);
            pst6.setString(1, username);
            pst6.setInt(2, bid);
            pst6.execute();
        }

        String sql4 = "SELECT READING_DURATION FROM READINGHISTORY WHERE BOOKID = ? AND USERNAME = ?";
        PreparedStatement pst3 = conn.prepareStatement(sql4);
        pst3.setInt(1, bid);
        pst3.setString(2, username);
        ResultSet rs3 = pst3.executeQuery();

        while (rs3.next()) {
            reading_history = rs3.getInt("READING_DURATION");
        }
        reading_history += time_purchased;
        String sql9 = "UPDATE READINGHISTORY SET READING_DURATION = ? WHERE BOOKID =? AND USERNAME = ?";
        PreparedStatement statement9 = conn.prepareStatement(sql9);
        statement9.setInt(1, reading_history);
        statement9.setInt(2, bid);
        statement9.setString(3, username);
        statement9.executeUpdate();

        String sql10 = "SELECT READING_COUNTS FROM BOOKINFO where BOOKID = ?";
        PreparedStatement pst10 = conn.prepareStatement(sql10);
        pst10.setInt(1, bid);
        ResultSet rs10 = pst10.executeQuery();

        String sql11 = "UPDATE USERINFO SET LASTBOOK_READ = ? WHERE USERNAME = ?";
        PreparedStatement statement11 = conn.prepareStatement(sql11);
        statement11.setInt(1, bid);
        statement11.setString(2, username);
        statement11.executeUpdate();
        int rtd = 0;
        String sql13 = "SELECT READING_TOTAL_DURATION FROM BOOKINFO where BOOKID = ?";
        PreparedStatement pst13 = conn.prepareStatement(sql13);
        pst13.setInt(1, bid);
        ResultSet rs13 = pst13.executeQuery();
        while (rs13.next()) {
            rtd = rs13.getInt("READING_TOTAL_DURATION");
            rtd += time_purchased;
            String sql12 = "UPDATE BOOKINFO SET READING_TOTAL_DURATION = ? WHERE BOOKID = ?";
            PreparedStatement statement12 = conn.prepareStatement(sql12);
            statement12.setInt(1, rtd);
            statement12.setInt(2, bid);
            statement12.executeUpdate();
        }
    }

